#include "libft.h"
#include <string.h>
#include <stdlib.h>

void	*ft_memalloc(size_t size)
{
	void	*fresh;

	if (!(fresh = (void *)malloc(size)))
		return (NULL);
	ft_bzero(fresh, size);
	return (fresh);
}
